person = {}                                     # создание пустого словаря
pool = 'Mika Taro NGU 4 5 5 4 5'                # переменная с текстом
pool = pool.split()                             # из ПЕРЕМЕННОЙ с ТЕКСТОМ создание списка с значениями
person['Name'] = pool[0]                        # Создаем ключ и присваиваем ему по ИНДЕКСУ значение из СПИСКА
person['Surname'] = pool[1]                     # Создаем ключ и присваиваем ему по ИНДЕКСУ значение из СПИСКА
person['Adication'] = pool[2]                   # Создаем ключ и присваиваем ему по ИНДЕКСУ значение из СПИСКА
person['marks'] = []                            # теперь: оценки мы ьудем в цикле по очереди добавлять  к ключу
                                        #
for _ in pool[3:]:                              #срезом указываем какую часть мы будем пихать в цикл
    person['marks'].append(int(_))              #добавляем к ключу оценки ( оценки с INT перевели в число т.к. изначально они у нас строкой были)
print(pool)                                     #['Mika', 'Taro', 'NGU', '4', '5', '5', '4', '5']
print(person)                                   #{'Name': 'Mika', 'Surname': 'Taro', 'Adication': 'NGU', 'marks': [4, 5, 5, 4, 5]}
