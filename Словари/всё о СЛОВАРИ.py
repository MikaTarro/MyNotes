x = {
    1: 'One',
    2: 'Two',
    3: 'Three'
}
print(x)

print(x.keys())                # ТОЛЬКО ключи = dict_keys([1, 2, 3])

for key in x.keys():                                 #  выводит все Ключи\значения
    #print('КЛЮЧИ и ЗНАЧЕНИЯ',key, x[key])                                         # КЛючи и значения
    print('ТОЛЬКО КЛЮЧИ',key)

print(x.values())              # ТОЛЬКО значения dict_values(['One', 'Two', 'Three'])
for value in x.values():                 #  выводит все Ключи\значения
    print('ТОЛЬКО ЗНАЧЕНИЯ',value)

print(x.items())              # dict_items([(1, 'One'), (2, 'Two'), (3, 'Three')])                КЛЮЧИ И ЗНАЧЕНИЯ
for key,value in x.items():                 #  ВСЕ ПАРЫ
    print(key, value)                                     # a,b = (1,3)